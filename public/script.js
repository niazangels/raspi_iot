$(document).keydown(function(e) {
    
    switch(e.keyCode){
    
    	/* Motion keys */
    	
    	case 37 : console.log('Left'); break;
    	case 38 : console.log('Up'); break;
    	case 39 : console.log('Right'); break;
    	case 40 : console.log('Down'); break;

    	/* Action keys */

    };
});